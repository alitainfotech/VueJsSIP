import { createRouter, createWebHistory } from "vue-router";

import Login from "@/views/LoginPage";
import Home from "@/views/HomePage";

const routes = [
  {
    path: "/",
    name: "Login",
    component: Login,
  },
  {
    path: "/home",
    name: "Home",
    component: Home,
  },
];

const router = createRouter({
  history: createWebHistory(),
  routes,
});

export default router;
